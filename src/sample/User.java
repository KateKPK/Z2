package sample;

public class User {
    private double K;
    private String Y;

    public User(double K, String Y) {
        this.K = K;
        this.Y = Y;
    }

    public User() {
    }

    public double getK() {
        return K;
    }

    public void setK(double K) {
        this.K = K;
    }

    public String getY() {
        return Y;
    }

    public void setY(String Y) {
        this.Y = Y;
    }

}
